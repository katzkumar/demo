public class Employee {

    private String name;

    private String employeeId;

    public String getName() {
        return name;

    }

    public String getEmployeeId() {
        return employeeId;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }
}
